function findObjects () {
  if (document.getElementById('body').contains('ytd-app')) {
    if (document.getElementById('ytd-app').contains('content')) {
      if (document.getElementById('content').contains('page-manager')) {
        if (document.getElementById('page-manager').contains('home')) {
          let content = document.getElementById('home')
        }
      }
    }
  }
  if (document.getElementById('body').contains('ytd-app')) {
    if (document.getElementById('ytd-app').contains('content')) {
      if (document.getElementById('content').contains('masthead-container')) {
        if (document.getElementById('masthead-container').contains('masthead')) {
          let masthead = document.getElementById('masthead')
        }
      }
    }
  }
  if (document.getElementById('body').contains('ytd-app')) {
    if (document.getElementById('ytd-app').contains('content')) {
      if (document.getElementById('content').contains('guide')) {
        if (document.getElementById('guide').contains('contentContainer')) {
          if (document.getElementById('contentContainer').contains('guide-wrapper')) {
            if (document.getElementById('guide-wrapper').contains('guide-content')) {
              let guide = document.getElementById('guide-content')
            }
          }
        }
      }
    }
  }
  if (document.getElementById('body').contains('ytd-app')) {
    if (document.getElementById('ytd-app').contains('content')) {
      if (document.getElementById('content').contains('masthead')) {
        if (document.getElementById('masthead').contains('container')) {
          if (document.getElementById('container').contains('search')) {
            if (document.getElementById('search').contains('search-form')) {
              if (document.getElementById('search-form').contains('container')) {
                let search = document.getElementById('container')
              }
            }
          }
        }
      }
    }
  }
  if (document.getElementById('body').contains('ytd-app')) {
    if (document.getElementById('ytd-app').contains('content')) {
      if (document.getElementById('content').contains('page-manager')) {
        if (document.getElementById('page-manager').contains('ytd-watch')) {
          if (document.getElementById('ytd-watch').contains('top')) {
            if (document.getElementById('top').contains('container')) {
              let playBackground = document.getElementById('container')
            }
          }
        }
      }
    }
  }
}
